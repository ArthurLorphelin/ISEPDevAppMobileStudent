package com.example.isepdevappmobilestudent.classes.DBtable;

import android.os.Parcel;
import android.os.Parcelable;

import androidx.annotation.NonNull;

public class Component implements Parcelable {
    private int id;
    private String name;
    private int componentManagerId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getComponentManagerId() {
        return componentManagerId;
    }

    public void setComponentManagerId(int componentManagerId) {
        this.componentManagerId = componentManagerId;
    }

    protected Component(Parcel in) {
    }

    public Component() {
    }

    public static final Creator<Component> CREATOR = new Creator<Component>() {
        @Override
        public Component createFromParcel(Parcel in) {
            return new Component(in);
        }

        @Override
        public Component[] newArray(int size) {
            return new Component[size];
        }
    };

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(@NonNull Parcel dest, int flags) {
    }
}
